package com.insta.comm.mapper;

public class PostPopupDto {
	private String user_id;
	private String post_num;
	private String board_num;
	private String file_path; 
	private String post_content;
	private String update_date;
	private String post_like_cnt;
	public String getUser_id() {
		return user_id;
	}
	public void setUser_id(String user_id) {
		this.user_id = user_id;
	}
	public String getPost_like_cnt() {
		return post_like_cnt;
	}
	public void setPost_like_cnt(String post_like_cnt) {
		this.post_like_cnt = post_like_cnt;
	}
	public String getPost_num() {
		return post_num;
	}
	public void setPost_num(String post_num) {
		this.post_num = post_num;
	}
	public String getBoard_num() {
		return board_num;
	}
	public void setBoard_num(String board_num) {
		this.board_num = board_num;
	}
	public String getFile_path() {
		return file_path;
	}
	public void setFile_path(String file_path) {
		this.file_path = file_path;
	}
	public String getPost_content() {
		return post_content;
	}
	public void setPost_content(String post_content) {
		this.post_content = post_content;
	}
	public String getUpdate_date() {
		return update_date;
	}
	public void setUpdate_date(String update_date) {
		this.update_date = update_date;
	}
	
	
}
